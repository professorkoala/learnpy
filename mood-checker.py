mood = input("What mood are you in: ")

if mood == "happy":
    print("It's great to see you happy!")
elif mood == "nervous":
    print("Take a deep breath 3 times.")
elif mood == "sad":
    print("Cheer up, mate!")
elif mood == "excited":
    print("You go girl!")
elif mood == "relaxed":
    print("Enjoy the peace.")
else:
    print("I don't recognize this mood.")
